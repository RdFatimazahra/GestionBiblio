function openAddBookPopup() {
    document.getElementById("addBookPopup").style.display = "block";
    document.body.style.overflow = "hidden"; /* Disable scrolling on body when popup is open */
}

function closeAddBookPopup() {
    document.getElementById("addBookPopup").style.display = "none";
    document.body.style.overflow = "auto"; /* Enable scrolling on body when popup is closed */
}


